#include "libft.h"

void *ft_realloc(void *ptr, size_t size)
{
	void *new;

	new = malloc(size);
	if (new == NULL)
		return (NULL);
	if (ptr != NULL)
	{
		ft_memcpy(new, ptr, size);
		free(ptr);
	}
	return (new);
}
